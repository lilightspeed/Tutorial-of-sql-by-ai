-- ============================================================
-- 1. SELECT 基本语法
-- ============================================================
-- 语法格式:
-- SELECT 列1, 列2, ...
-- FROM 表名
-- WHERE 条件;
--
-- 执行顺序:
-- 1. FROM   — 确定从哪张表取数据
-- 2. WHERE  — 过滤符合条件的行
-- 3. SELECT — 选择要显示的列

-- 使用数据库
USE shop_db;

-- ============================================================
-- 2. 查询指定列
-- ============================================================
-- 只选择你需要的列，而不是 *（所有列）
-- 语法: SELECT 列名1, 列名2 FROM 表名;

-- 查询客户的姓名和城市
SELECT customer_name, city FROM customers;

-- 查询商品名称和价格
SELECT product_name, price FROM products;

-- ============================================================
-- 3. 查询所有列
-- ============================================================
-- 使用 * 代表所有列（实际开发中建议明确列出需要的列）
-- 语法: SELECT * FROM 表名;

-- 查询所有商品信息
SELECT * FROM products;
select product_name, price from products;
-- ============================================================
-- 4. WHERE 条件过滤
-- ============================================================
-- WHERE 子句用于过滤满足条件的行
-- 语法: SELECT 列名 FROM 表名 WHERE 条件;
--
-- 常用比较运算符:
-- +-----------+----------------+---------------------+
-- | 运算符    | 说明           | 示例                |
-- +-----------+----------------+---------------------+
-- | =         | 等于           | city = '北京'       |
-- | != 或 <>  | 不等于         | status != '已完成'  |
-- | >         | 大于           | price > 100         |
-- | <         | 小于           | price < 50          |
-- | >=        | 大于等于       | stock >= 100        |
-- | <=        | 小于等于       | salary <= 15000     |
-- +-----------+----------------+---------------------+

-- 查询北京的客户
SELECT * FROM customers WHERE city = '北京';

-- 查询价格大于 1000 的商品
SELECT product_name, price FROM products WHERE price > 1000 order by price asc;

-- 查询已完成的订单
SELECT order_id, order_date, total_amount FROM orders ;

-- ============================================================
-- 5. DISTINCT 去重
-- ============================================================
-- DISTINCT 用于消除重复行，只返回唯一的值
-- 语法: SELECT DISTINCT 列名 FROM 表名;

-- 查询客户所在的城市（去重）
SELECT DISTINCT city FROM customers;

-- 查询商品的分类ID（去重）
SELECT DISTINCT category_id FROM products;

-- ============================================================
-- 6. LIMIT 限制行数
-- ============================================================
-- LIMIT 用于限制返回的记录数量，常用于分页或预览数据
-- 语法: SELECT 列名 FROM 表名 LIMIT N;           -- 返回前 N 行
--       SELECT 列名 FROM 表名 LIMIT N OFFSET M;  -- 跳过前 M 行，返回 N 行

-- 只查看前 3 个商品
SELECT product_name, price FROM products LIMIT 3;

-- 分页：跳过前 3 条，取 3 条（第 4-6 条）
SELECT product_name, price FROM products LIMIT 3 , 3;

-- ============================================================
-- 7. 别名 AS
-- ============================================================
-- 使用 AS 为列或表设置别名，让输出更易读
-- 语法: SELECT 列名 AS 别名 FROM 表名 AS 表别名;

-- 为列设置中文别名
SELECT
    product_name AS 商品名称,
    price AS 价格,
    stock AS 库存
FROM products
LIMIT 5;

-- 在表达式中使用别名
SELECT
    product_name AS 商品名称,
    price AS 单价,
    stock AS 库存数量,
    price * stock AS 库存总价值
FROM products
LIMIT 5;

-- ============================================================
-- 8. 计算列
-- ============================================================
-- SELECT 中可以使用算术运算符对列进行计算
--
-- +----------+--------+------------------+
-- | 运算符   | 说明   | 示例             |
-- +----------+--------+------------------+
-- | +        | 加     | price + 10       |
-- | -        | 减     | price - discount |
-- | *        | 乘     | price * quantity |
-- | /        | 除     | total / count    |
-- +----------+--------+------------------+

-- 计算每个商品打 8 折后的价格
SELECT
    product_name AS 商品名称,
    price AS 原价,
    price * 0.8 AS 折后价
FROM products
LIMIT 5;

