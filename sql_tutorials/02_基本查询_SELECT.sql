-- 题目1（简单）: 查询所有员工的姓名和部门
SELECT employee_name, department FROM employees;

-- 题目2（简单）: 查询所有商品分类的名称和描述
SELECT category_name, description FROM categories;

-- 题目3（中等）: 查询价格低于 200 元的商品名称和价格
SELECT product_name, price FROM products WHERE price < 200;

-- 题目4（中等）: 查询库存超过 100 件的商品名称和库存，并给列起中文别名
SELECT
    product_name AS 商品名称,
    stock AS 库存数量
FROM products
WHERE stock > 100;

-- 题目5（中等）: 查询所有订单状态（去重），看看一共有多少种状态
SELECT DISTINCT status FROM orders;

