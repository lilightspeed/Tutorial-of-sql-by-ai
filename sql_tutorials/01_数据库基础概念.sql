-- 6. 数据类型
-- ============================================================
-- 每列都有特定的数据类型，决定了该列能存储什么数据
--
-- MySQL 常用数据类型:
--
-- 数值类型:
-- +----------------+------------------+-------------------------+
-- | 类型           | 说明             | 示例                    |
-- +----------------+------------------+-------------------------+
-- | INT            | 整数             | 1, 100, -5              |
-- | BIGINT         | 大整数           | 9999999999              |
-- | DECIMAL(M,D)   | 精确小数         | DECIMAL(10,2) -> 12345678.90 |
-- | FLOAT          | 浮点数（不精确） | 3.14                    |
-- +----------------+------------------+-------------------------+
--
-- 字符串类型:
-- +----------------+------------------+-------------------------+
-- | 类型           | 说明             | 示例                    |
-- +----------------+------------------+-------------------------+
-- | VARCHAR(N)     | 可变长度字符串   | VARCHAR(50) -> '张三'   |
-- | CHAR(N)        | 固定长度字符串   | CHAR(2) -> '男'         |
-- | TEXT           | 长文本           | 文章内容                |
-- +----------------+------------------+-------------------------+
--
-- 日期时间类型:
-- +----------------+------------------+-------------------------+
-- | 类型           | 说明             | 示例                    |
-- +----------------+------------------+-------------------------+
-- | DATE           | 日期             | 2024-01-15              |
-- | DATETIME       | 日期时间         | 2024-01-15 10:30:00     |
