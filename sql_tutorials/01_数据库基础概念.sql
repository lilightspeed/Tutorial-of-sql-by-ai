-- 1. 什么是数据库？
-- ============================================================
-- 数据库（Database）是按照一定组织方式存储和管理数据的仓库。
--
-- 生活中的例子:
-- +------------+------------------------+
-- | 场景       | 数据示例               |
-- +------------+------------------------+
-- | 银行       | 账户信息、交易记录     |
-- | 淘宝       | 商品信息、订单、用户   |
-- | 学校       | 学生信息、课程、成绩   |
-- | 医院       | 病历、处方、药品       |
-- +------------+------------------------+
--
-- 数据库管理系统（DBMS）是用来管理数据库的软件:
-- +-------------+--------------------------------+
-- | DBMS        | 特点                           |
-- +-------------+--------------------------------+
-- | MySQL       | 开源免费，应用最广泛           |
-- | PostgreSQL  | 功能强大，支持复杂数据类型     |
-- | Oracle      | 商业数据库，大型企业使用       |
-- | SQL Server  | 微软产品，与 .NET 集成好       |
-- | SQLite      | 轻量级，嵌入式，无需服务器     |
-- +-------------+--------------------------------+
--
-- 本教程使用 MySQL 进行教学
-- ============================================================

-- ============================================================
-- 2. 关系型数据库
-- ============================================================
-- 关系型数据库（RDBMS）是目前最主流的数据库类型
-- 数据以表（Table）的形式组织
--
-- 核心概念:
-- +-------------------------------------------------------+
-- |                    数据库 (Database)                   |
-- |                                                       |
-- |   +--------------+  +--------------+                 |
-- |   |   表 (Table) |  |   表 (Table) |   ...           |
-- |   |              |  |              |                 |
-- |   |  行 (Row)    |  |  行 (Row)    |                 |
-- |   |  行 (Row)    |  |  行 (Row)    |                 |
-- |   |  ...         |  |  ...         |                 |
-- |   +--------------+  +--------------+                 |
-- |         ^                                            |
-- |    列 (Column)                                       |
-- +-------------------------------------------------------+
--
-- 表（Table）: 数据库中存储数据的基本单位，类似于 Excel 工作表
-- 行（Row）: 表中的每一行代表一条记录（Record）
-- 列（Column）: 表中的每一列代表一个字段（Field），有特定的数据类型
-- ============================================================

