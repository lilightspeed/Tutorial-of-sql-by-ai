-- 4. 主键（Primary Key）
-- ============================================================
-- 主键是表中唯一标识每条记录的列（或列组合）
--
-- 主键的特点:
-- +----------+--------------------------------+
-- | 特点     | 说明                           |
-- +----------+--------------------------------+
-- | 唯一性   | 每条记录的主键值都不相同       |
-- | 非空     | 主键列不允许为 NULL            |
-- | 稳定     | 主键值不应该频繁修改           |
-- +----------+--------------------------------+
--
-- 例子: 在 customers 表中，customer_id 是主键
-- - 张三的 customer_id = 1
-- - 李四的 customer_id = 2
-- - 通过 customer_id 可以唯一确定一个客户
--
-- 常见主键设计:
-- +----------+----------------+----------+------------------+
-- | 方式     | 示例           | 优点     | 缺点             |
-- +----------+----------------+----------+------------------+
-- | 自增整数 | 1, 2, 3, ...   | 简单高效 | 数据迁移时冲突   |
-- | UUID     | 'a1b2c3d4-...' | 全球唯一 | 占用空间大       |
-- | 业务编号 | 身份证号、学号 | 有业务含义 | 可能变化       |
-- +----------+----------------+----------+------------------+

-- 主键的唯一性示例
-- 每个 customer_id 都是唯一的
SELECT customer_id, customer_name, city FROM customers;

-- ============================================================
-- 5. 外键（Foreign Key）
-- ============================================================
-- 外键是一个表中的列，它引用了另一个表的主键
-- 用于建立表与表之间的关联关系
--
-- 例子:
-- customers 表                 orders 表
-- +--------------+           +------------------+
-- | customer_id  |<----------| customer_id (FK) |
-- | customer_name|           | order_id (PK)    |
-- | email        |           | order_date       |
-- +--------------+           | total_amount     |
--                            +------------------+
--
-- PK = Primary Key (主键)
-- FK = Foreign Key (外键)
--
-- 外键确保了引用完整性：
-- 订单中的 customer_id 必须在 customers 表中存在

-- 外键关系示例
-- orders 表的 customer_id 引用了 customers 表的 customer_id
SELECT
    o.order_id,
    o.customer_id,    -- 外键
    c.customer_name,  -- 通过外键关联到客户名
    o.total_amount
FROM orders o
JOIN customers c ON o.customer_id = c.customer_id
LIMIT 5;

